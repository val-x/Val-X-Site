// Add categories helper
export const getCategories = () => {
  const categories = new Set();
  getAllProjects.forEach((project) => {
    categories.add(project.category);
  });
  return ["all", ...Array.from(categories)];
};

// Add stats helper
export const getProjectStats = () => {
  return {
    totalProjects: getAllProjects.length,
    totalUsers: getAllProjects.reduce(
      (sum, project) => sum + parseInt(project.stats.users), 
      0
    ),
    averageRating: (
      getAllProjects.reduce(
        (sum, project) => sum + parseFloat(project.stats.rating), 
        0
      ) / getAllProjects.length
    ).toFixed(1),
    categoryCounts: getAllProjects.reduce(
      (acc, project) => {
      acc[project.category] = (acc[project.category] || 0) + 1;
      return acc;
      },
      { all: getAllProjects.length }
    ),
  };
};

